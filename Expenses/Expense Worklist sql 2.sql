ON ( EXP.EXPENSE_REPORT_ID = EER.EXPENSE_REPORT_ID )
  LEFT JOIN EXM_EXPENSE_DISTS EERDIST
ON ( EERDIST.EXPENSE_REPORT_ID = EXP.EXPENSE_REPORT_ID
