  LEFT JOIN EXM_EXPENSE_TYPES EETYP
ON EXP.EXPENSE_TYPE_ID = EETYP.EXPENSE_TYPE_ID
  LEFT JOIN PER_PERSON_NAMES_F_V PPNFV
ON EXP.PERSON_ID = PPNFV.PERSON_ID
